public class forloop {
    public static void main(String[]args){
        for(int k=0;k<=10;k++){
            System.out.println(k);
            JOptionPane.showMessageDialog(null,k);
        }
        for(int l=0;l<=20;l++){
            System.out.println(l);
            JOptionPane.showMessageDialog(null,l);
        }
    }
}
